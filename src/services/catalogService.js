import api, { isSuccess } from './ApiService'

const base_path = '/partner/restaurant/catalog'

//#######################################################################
//#######################################################################
//#### FINDS ############################################################
//#######################################################################
//#######################################################################

export const get = () => {
	return api
		.get(`${base_path}`)
		.then((response) => {
			if (isSuccess(response)) {
				return response.data
			}
			return Promise.reject(response.data)
		})
		.catch((err) => {
			return Promise.reject(err)
		})
}

